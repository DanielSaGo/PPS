numero = int(input())
if numero >= 9 and numero <= 10:
    print("A")
elif numero >= 8 and numero < 9:
    print("B")
elif numero >= 7 and numero < 8:
    print("C")
elif numero >= 6 and numero < 7:
    print("D")
elif numero >= 0 and numero < 6:
    print("F")
